package ListDemo;

public class Student {
    private  String name;
    private  int age ;
    public Student(String name , int age ){
        this.name = name;
        this.age = age ;
    }
    public void setname(){
        this.name = name ;
    }
    public  void setage(){
        this.age = age ;
    }
    public String getname(){
        return name;
    }
    public int getage(){
        return  age;
    }
}
